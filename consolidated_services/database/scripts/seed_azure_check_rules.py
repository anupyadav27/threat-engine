def _conn():
    return psycopg2.connect(
        host=os.getenv("CHECK_DB_HOST", "localhost"),
        port=int(os.getenv("CHECK_DB_PORT", "5432")),
        dbname=os.getenv("CHECK_DB_NAME", "threat_engine_check"),
        user=os.getenv("CHECK_DB_USER", "postgres"),
        password=os.getenv("CHECK_DB_PASSWORD", ""),
    )


def _r(
    rule_id, service, resource, severity, title, description,
    remediation, cis=None, nist=None, mitre_tactics=None,
    mitre_techniques=None, threat_category=None,
):
    """Helper to build a rule dict with all required fields."""
    cf = {}
    if cis:
        cf["cis_azure_1_5"] = cis if isinstance(cis, list) else [cis]
    if nist:
        cf["nist_800_53"] = nist if isinstance(nist, list) else [nist]
    return {
        "rule_id": rule_id,
        "service": service,
        "provider": "azure",
        "resource": resource,
        "severity": severity,
        "title": title,
        "description": description,
        "remediation": remediation,
        "compliance_frameworks": cf,
        "mitre_tactics": mitre_tactics or [],
        "mitre_techniques": mitre_techniques or [],
        "threat_category": threat_category or "misconfiguration",
    }


