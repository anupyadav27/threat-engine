INSERT_SQL = """
    INSERT INTO rule_metadata (
        rule_id, service, provider, resource, severity,
        title, description, remediation,
        compliance_frameworks, mitre_tactics, mitre_techniques,
        threat_category, source, metadata_source, version,
        tenant_id, customer_id
    ) VALUES (
        %s, %s, %s, %s, %s,
        %s, %s, %s,
        %s, %s, %s,
        %s, %s, %s, %s,
        NULL, NULL
    )
    ON CONFLICT (rule_id, customer_id, tenant_id) DO UPDATE SET
        service             = EXCLUDED.service,
        provider            = EXCLUDED.provider,
        resource            = EXCLUDED.resource,
        severity            = EXCLUDED.severity,
        title               = EXCLUDED.title,
        description         = EXCLUDED.description,
        remediation         = EXCLUDED.remediation,
        compliance_frameworks = EXCLUDED.compliance_frameworks,
        mitre_tactics       = EXCLUDED.mitre_tactics,
        mitre_techniques    = EXCLUDED.mitre_techniques,
        threat_category     = EXCLUDED.threat_category,
        updated_at          = NOW()
"""


def seed() -> None:
    conn = _conn()
    inserted = 0
    updated = 0

    try:
        for r in ALL_RULES:
            with conn.cursor() as cur:
                cur.execute(
                    "SELECT id FROM rule_metadata WHERE rule_id=%s AND tenant_id IS NULL AND customer_id IS NULL",
                    (r["rule_id"],),
                )
                exists = cur.fetchone()

                cur.execute(INSERT_SQL, (
                    r["rule_id"], r["service"], "azure", r["resource"], r["severity"],
                    r["title"], r["description"], r["remediation"],
                    Json(r["compliance_frameworks"]),
                    Json(r["mitre_tactics"]),
                    Json(r["mitre_techniques"]),
                    r["threat_category"], "azure_cis_seed", "azure_cis_seed", "1.0",
                ))
                if exists:
                    updated += 1
                else:
                    inserted += 1

            conn.commit()

        print(f"\nDone: {inserted} inserted, {updated} updated ({len(ALL_RULES)} total)")
        by_service = {}
        for r in ALL_RULES:
            by_service[r["service"]] = by_service.get(r["service"], 0) + 1
        for svc, cnt in sorted(by_service.items()):
            print(f"  {svc:20s}: {cnt}")

    finally:
        conn.close()


if __name__ == "__main__":
    print(f"Seeding {len(ALL_RULES)} Azure check rules into rule_metadata ...")
    seed()
