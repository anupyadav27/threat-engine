export default function CspIcon({ provider, size = 16, className, style, title }) {
  const key = (provider || '').toLowerCase();
  const entry = PROVIDERS[key];

  if (!entry) {
    // Unknown provider — neutral gray square with abbreviation
    return (
      <span
        title={title || provider}
        className={className}
        style={{
          display: 'inline-flex', alignItems: 'center', justifyContent: 'center',
          width: size, height: size, borderRadius: 3,
          background: 'rgba(148,163,184,0.2)',
          fontSize: size * 0.5, fontWeight: 700, color: '#94a3b8',
          letterSpacing: '-0.03em', lineHeight: 1,
          flexShrink: 0, userSelect: 'none',
          ...style,
        }}
      >
        {(provider || '?').slice(0, 2).toUpperCase()}
      </span>
    );
  }

  return (
    <svg
      width={size}
      height={size}
      viewBox="0 0 24 24"
      fill={entry.color}
      aria-label={title || entry.label}
      title={title || entry.label}
      className={className}
      style={{ flexShrink: 0, display: 'block', ...style }}
    >
      <path d={entry.path} />
    </svg>
  );
}

/**
 * ProviderCell — icon + truncated account ID, used in table rows.
 *
 * <ProviderCell provider="aws" accountId="123456789012" />
 */
