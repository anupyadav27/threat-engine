def _iam_block(applicable: bool = False, modules: list = None) -> dict:
    return {"applicable": applicable, "modules": modules or []}

def write_rule(rule: dict[str, Any]) -> Path:
    rid   = rule["rule_id"]
    svc   = rule.get("service", rid.split(".")[1])
    out_d = OUT_DIR / svc
    out_d.mkdir(parents=True, exist_ok=True)

    cfw = rule.get("compliance_frameworks", {})
    if isinstance(cfw, list):
        cfw = {}

    doc = {
        "rule_id":             rid,
        "service":             svc,
        "provider":            "alicloud",
        "check_type":          rule.get("check_type", "log"),
        "severity":            rule.get("severity", "medium"),
        "title":               rule.get("title", rid),
        "description":         textwrap.dedent(rule.get("description", "")).strip(),
        "rationale":           rule.get("rationale", "Monitoring this operation is essential for detecting adversary activity targeting AliCloud resources. Unauthorized or unexpected API calls may indicate account compromise, privilege escalation, or an active attack chain in progress."),
        "threat_category":     rule.get("threat_category", ""),
        "mitre_tactics":       rule.get("mitre_tactics", []),
        "mitre_techniques":    rule.get("mitre_techniques", []),
        "risk_score":          rule.get("risk_score", 50),
        "resource":            rule.get("resource", "alicloud_resource"),
        "source":              "alicloud_ciem_generator",
        "is_active":           True,
        "domain":              rule.get("domain", "identity_and_access_management"),
        "action_category":     rule.get("action_category", "audit_activity"),
        "log_source_type":     rule.get("log_source_type", ""),
        "posture_category":    rule.get("posture_category", "iam_posture"),
        "threat_tags":         rule.get("threat_tags", rule.get("mitre_techniques", []) + rule.get("mitre_tactics", [])),
        "risk_indicators":     rule.get("risk_indicators", {
            "actor_type":    "cloud_principal",
            "action_type":   "write",
            "target_type":   "cloud_resource",
            "blast_radius":  "account",
            "stealth_risk":  "medium",
        }),
        "iam_security":        rule.get("iam_security", _iam_block("iam" in svc or "iam" in rule.get("domain", ""))),
        "data_security":       rule.get("data_security", {"applicable": "data" in svc or "datasec" in svc or "objectstorage" in svc}),
        "compliance_frameworks": cfw,
        "remediation":         rule.get("remediation", "Review and investigate this activity in the AliCloud Console and take corrective action."),
        "references":          rule.get("references", []),
        "check_config":        rule.get("check_config", {}),
        "version":             "1.0",
    }

    import yaml as _yaml
    path = out_d / f"{rid}.yaml"
    path.write_text(_yaml.dump(doc, default_flow_style=False, allow_unicode=True, sort_keys=False))
    return path


