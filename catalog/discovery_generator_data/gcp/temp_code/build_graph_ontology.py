def _classify(service: str, resource_type: str) -> tuple[str, str, float, str]:
    """
    Classify (service, resource_type) → (category, generic_node_type, confidence, notes).
    """
    svc_l  = service.lower()
    res_l  = resource_type.lower()

    for svc_pat, res_pat, category, node_type, conf in _RULES:
        if re.search(svc_pat, svc_l) and re.search(res_pat, res_l):
            # Boost confidence slightly if both patterns are non-wildcard
            is_both_specific = svc_pat != r'.*' and res_pat != r'.*'
            final_conf = min(conf + (0.02 if is_both_specific else 0.0), 1.0)
            notes = ''
            if final_conf < 0.6:
                notes = f'Low confidence: service={service}, resource={resource_type}'
            elif res_pat == r'.*' and svc_pat != r'.*':
                notes = 'Resource pattern wildcard — classified by service name only.'
            elif svc_pat == r'.*':
                notes = 'Service pattern wildcard — classified by resource name only.'
            return category, node_type, round(final_conf, 3), notes

    return 'other', 'other.unknown', 0.40, \
           f'No matching rule for service={service}, resource_type={resource_type}'


def _id_kind_from_catalog(rdata: dict) -> str:
    """Map catalog identifier.kind → ontology id_kinds value."""
    kind_map = {
        'full_name':   'gcp_full_name',
        'constructed': 'constructed',
        'tuple':       'tuple',
    }
    catalog_kind = rdata.get('identifier', {}).get('kind', 'tuple')

    # Special: if constructed kind is "url" by nature (e.g. reviewedSite)
    transforms = rdata.get('identifier', {}).get('transforms', [])
    if catalog_kind == 'constructed' and transforms:
        for t in transforms:
            if t.get('fn') == 'url_encode':
                return 'constructed'   # URL-encoded constructed

    # Check part_sources for email fields
    parts = rdata.get('identifier', {}).get('parts', [])
    for p in parts:
        if 'email' in p.lower() or 'account' in p.lower():
            # Could be email; defer to service
            pass

    return kind_map.get(catalog_kind, 'gcp_full_name')


