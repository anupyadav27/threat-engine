def build_ontology() -> dict:
    return {
        "version": "1.0",
        "generated_at": _now_iso(),
        "categories": sorted(CATEGORIES, key=lambda x: x['category']),
        "generic_node_types": sorted(GENERIC_NODE_TYPES, key=lambda x: x['type']),
        "edge_types": sorted(EDGE_TYPES, key=lambda x: x['type']),
        "id_kinds": ID_KINDS,
    }


def build_taxonomy(base_dir: Path, ontology: dict) -> dict:
    """Read all step5 catalogs and produce the taxonomy mappings."""
    valid_node_types = {n['type'] for n in ontology['generic_node_types']}
    valid_categories = {c['category'] for c in ontology['categories']}

    mappings = []

    catalog_files = sorted(
        f for f in base_dir.rglob('step5_resource_catalog_inventory_enrich.json')
    )

    for cat_path in catalog_files:
        try:
            doc = json.load(open(cat_path))
        except Exception:
            continue

        services_block = doc.get('services', {})
        for svc_name, svc_data in services_block.items():
            resources = svc_data.get('resources', {})
            for rtype, rdata in sorted(resources.items()):
                category, node_type, confidence, notes = _classify(svc_name, rtype)

                # Validate against ontology (safety net)
                if node_type not in valid_node_types:
                    node_type = 'other.unknown'
                    confidence = min(confidence, 0.50)
                if category not in valid_categories:
                    category = 'other'

                # Identifier kind
                id_kind = _id_kind_from_catalog(rdata)

                # Special override: serviceAccounts → email id_kind
                if 'serviceaccount' in rtype.lower() and 'iam' in svc_name.lower():
                    id_kind = 'email'

                # Dataset names — from inventory op keys
                inv_ops    = rdata.get('inventory', {}).get('ops', [])
                enrich_ops = rdata.get('inventory_enrich', {}).get('ops', [])

                inv_datasets    = [op['op'] for op in inv_ops if op.get('op')]
                enrich_datasets = [op['op'] for op in enrich_ops if op.get('op')]

                if not inv_datasets:
                    inv_datasets = [f'gcp.{svc_name}.{rtype}.inventory']
                    if not notes:
                        notes = 'inferred_dataset_name'
                    else:
                        notes += '; inferred_dataset_name'

                if not enrich_datasets:
                    enrich_datasets = [f'gcp.{svc_name}.{rtype}.enrich']

                mappings.append({
                    "provider":               "gcp",
                    "service":                svc_name,
                    "resource_type":          rtype,
                    "category":               category,
                    "generic_node_type":      node_type,
                    "default_identifier_kind": id_kind,
                    "inventory": {
                        "datasets":        sorted(inv_datasets),
                        "enrich_datasets": sorted(enrich_datasets),
                    },
                    "confidence": confidence,
                    "notes":      notes,
                })

    # Sort: service → resource_type
    mappings.sort(key=lambda m: (m['service'], m['resource_type']))

    return {
        "version": "1.0",
        "generated_at": _now_iso(),
        "mappings": mappings,
    }


