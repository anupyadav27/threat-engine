def main():
    parser = argparse.ArgumentParser(
        description='Build GCP Graph Ontology + Resource Taxonomy'
    )
    parser.add_argument('--base', type=Path, default=BASE_DIR,
                        help=f'Base GCP data directory (default: {BASE_DIR})')
    args = parser.parse_args()

    base = args.base

    print('Building GCP graph ontology...')
    ontology = build_ontology()
    ont_path = base / 'gcp_graph_ontology.json'
    with open(ont_path, 'w') as f:
        json.dump(ontology, f, indent=2)
    print(f'  Written: {ont_path}')

    print('Building GCP resource taxonomy...')
    taxonomy = build_taxonomy(base, ontology)
    tax_path = base / 'gcp_resource_taxonomy.json'
    with open(tax_path, 'w') as f:
        json.dump(taxonomy, f, indent=2)
    print(f'  Written: {tax_path}')

    _print_summary(ontology, taxonomy)


if __name__ == '__main__':
    main()
