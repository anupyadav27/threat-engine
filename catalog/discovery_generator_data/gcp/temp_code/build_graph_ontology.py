def _print_summary(ontology: dict, taxonomy: dict):
    mappings   = taxonomy['mappings']
    total      = len(mappings)
    low_conf   = [m for m in mappings if m['confidence'] < 0.6]
    categories = {}
    for m in mappings:
        categories[m['category']] = categories.get(m['category'], 0) + 1

    print('=' * 70)
    print('GCP GRAPH ONTOLOGY + TAXONOMY BUILD SUMMARY')
    print('=' * 70)
    print(f'  Generic node types : {len(ontology["generic_node_types"])}')
    print(f'  Edge types         : {len(ontology["edge_types"])}')
    print(f'  Categories         : {len(ontology["categories"])}')
    print(f'  Total mappings     : {total}')
    print()
    print('  Mappings by category:')
    for cat, cnt in sorted(categories.items(), key=lambda x: -x[1]):
        print(f'    {cat:<20} {cnt:4d}')
    print()
    print(f'  Low-confidence mappings (< 0.6): {len(low_conf)}')
    if low_conf:
        print('  Top low-confidence:')
        for m in sorted(low_conf, key=lambda x: x['confidence'])[:15]:
            print(f'    [{m["confidence"]:.2f}]  {m["service"]}.{m["resource_type"]}'
                  f'  →  {m["generic_node_type"]}  | {m["notes"]}')
    print('=' * 70)


